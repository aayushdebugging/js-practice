console.log("Using for loop:");
for (let i = 1; i <= 5; i++) {
    console.log(i);
}

// Using while loop
console.log("Using while loop:");
let i = 1;
while (i <= 5) {
    console.log(i);
    i++; 
}

// Using do-while loop
console.log("Using do-while loop:");
i = 1; 
do {
    console.log(i);
    i++;
} while (i <= 5);
